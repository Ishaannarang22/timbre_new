# P1-4: Quote pre-generated at /twiml time (while Twilio sets up the call), keyed by
# CallSid, so the greeting is instant and the exact quote is guaranteed into the prompt.
# Values are (quote, created_monotonic) so abandoned calls (caller never answers, so /ws
# never pops the key) can be evicted instead of leaking forever. Tokens for the per-call
