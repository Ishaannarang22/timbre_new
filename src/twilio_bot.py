@app.api_route("/twiml", methods=["GET", "POST"])
async def twiml(request: Request) -> PlainTextResponse:
    """Twilio fetches this when the outbound call connects. We hand it back a
    <Connect><Stream> pointed at our websocket, so the call's audio is bridged to Pipecat."""
    # Twilio POSTs form-encoded data; fall back to query params so a missing
    # python-multipart (or a GET-configured webhook) can't 500 the webhook.
    try:
        form = await request.form()
        call_sid = form.get("CallSid") or request.query_params.get("CallSid") or "unknown"
        direction = form.get("Direction") or request.query_params.get("Direction") or ""
        from_number = form.get("From") or request.query_params.get("From") or ""
    except Exception:  # noqa: BLE001
        call_sid = request.query_params.get("CallSid", "unknown")
        direction = request.query_params.get("Direction", "")
        from_number = request.query_params.get("From", "")
    # INBOUND vs OUTBOUND persona: Twilio sends Direction=inbound for an incoming call
    # (Ishaan dialed the number) and Direction=outbound-api for our REST-placed 7 AM call.
    # We map that to a simple mode the pipeline understands and pass it through to /ws.
    # Default to "outbound" so the existing morning flow is unchanged if Direction is absent.
    mode = "inbound" if str(direction).lower() == "inbound" else "outbound"
    # Caller authorization (CONTRACT.md): OUTBOUND is always authorized (we dialed the owner);
    # INBOUND is authorized only if From is on the allowlist. We compute it here and pass it +
    # the caller number to /ws via <Parameter> (same trusted channel as token/mode). /ws uses
    # `authorized` to decide whether to offer tools, and `caller` to key cross-call memory.
    caller = _normalize_e164(from_number)
    authorized = is_authorized(direction, from_number)
    # P1-4: evict abandoned entries first so QUOTES/WS_TOKENS can't grow unbounded.
    _evict_stale()
    now = time.monotonic()
    # OUTBOUND pre-generates the motivational quote while Twilio sets up the call. INBOUND
    # is an assistant persona with NO quote, so we skip that NVIDIA call (less latency, no
    # wasted credits) and store an empty string; /ws's inbound path never uses it.
    QUOTES[call_sid] = ((generate_quote() if mode == "outbound" else ""), now)
    # Security: mint a per-call token, store it server-side keyed by CallSid, and embed it
    # in the ws URL. /ws rejects any connection whose token doesn't match — so a stranger
    # who finds the public wss endpoint can't open a session and burn STT/LLM/TTS credits.
    token = secrets.token_urlsafe(24)
    WS_TOKENS[call_sid] = (token, now)
    host = request.headers.get("host")
    # Per Twilio docs, <Stream url> does NOT support query-string parameters — they're
    # dropped on the wss upgrade, and a raw "&" between two params is also invalid XML
    # (the cause of the inbound "application error"). So the URL is clean, and ALL custom
    # data (per-call token + persona mode) is passed via <Parameter> children, which Twilio
    # echoes in the 'start' event's start.customParameters — the officially-supported channel
    # that /ws reads. https://www.twilio.com/docs/voice/twiml/stream#custom-parameters
    ws_url = f"wss://{host}/ws"
    logger.info(
        f"/twiml call_sid={call_sid} mode={mode} authorized={authorized} "
        f"-> stream {ws_url} (token via <Parameter>)"
    )
    # `caller` is the NORMALIZED From (digits + optional leading '+'), so it's XML-safe; we pass
    # it (not the raw From) both for memory keying and to avoid any XML-injection surface.
    xml = (
        '<?xml version="1.0" encoding="UTF-8"?>'
        f"<Response><Connect><Stream url=\"{ws_url}\">"
        f"<Parameter name=\"token\" value=\"{token}\"/>"
        f"<Parameter name=\"mode\" value=\"{mode}\"/>"
        f"<Parameter name=\"authorized\" value=\"{'true' if authorized else 'false'}\"/>"
        f"<Parameter name=\"caller\" value=\"{caller}\"/>"
        "</Stream></Connect></Response>"
    )
    return PlainTextResponse(xml, media_type="application/xml")


@app.websocket("/ws")
async def ws(websocket: WebSocket) -> None:
    await websocket.accept()
    # P1-4: harden startup. The caller may not answer, or may hang up before Twilio sends
    # the 'start' frame; receive_text() then raises (disconnect) or returns junk. Wrap both
    # reads so a dropped/early call logs and returns cleanly instead of throwing a 500.
    try:
        await websocket.receive_text()  # 'connected'
        start = json.loads(await websocket.receive_text())  # 'start'
        stream_sid = start["start"]["streamSid"]
        call_sid = start["start"]["callSid"]
    except Exception as e:  # noqa: BLE001
        logger.warning(f"/ws closed before start handshake completed: {type(e).__name__}: {e}")
        try:
            await websocket.close()
        except Exception:  # noqa: BLE001
            pass
        return

    # Security: per-call token, DUAL-CHANNEL + fail-safe. /twiml minted a token for this
    # CallSid and delivered it BOTH on the ws URL query string AND as a <Stream><Parameter>
    # (which Twilio echoes in start.start.customParameters). We accept a match from EITHER
    # channel, so even if the query string were dropped on the wss upgrade, the real call
    # still passes (customParameters always carries it). We reject (close 1008) ONLY when a
    # token was minted for this CallSid and NEITHER channel presents a matching value — that
    # blocks a no-token scanner without risking the real 7 AM call.
    # (The offline sim mints a real token via /twiml and presents it on at least one channel,
    # so it exercises this exact auth path — no test backdoor that could weaken prod auth.)
    expected = WS_TOKENS.pop(call_sid, (None, 0.0))[0]
    if expected is not None:
        qs_token = websocket.query_params.get("token")
        try:
            cp_token = start["start"].get("customParameters", {}).get("token")
        except (AttributeError, TypeError):
            cp_token = None
        ok = any(
            t is not None and secrets.compare_digest(t, expected)
            for t in (qs_token, cp_token)
        )
        if not ok:
            logger.warning(f"/ws rejected: bad/missing token for call_sid={call_sid}")
            try:
                await websocket.close(code=1008)  # policy violation
            except Exception:  # noqa: BLE001
                pass
            return

    # Persona mode (inbound = Ishaan dialed us; outbound = the 7 AM call). Read it from the
    # SAME dual channels as the token: start.customParameters (Twilio-delivered <Parameter>)
    # first, then the ws URL query string. Default "outbound" so the morning flow is unchanged
    # if mode is absent. We sanitize to the two known values.
    try:
        cp_mode = start["start"].get("customParameters", {}).get("mode")
    except (AttributeError, TypeError):
        cp_mode = None
    mode = (cp_mode or websocket.query_params.get("mode") or "outbound").lower()
    if mode != "inbound":
        mode = "outbound"

    # Caller authorization + caller number, read from the SAME dual channels as token/mode.
    # `authorized` decides whether the agent is offered the Mac-control tools at all (an
    # unauthorized inbound caller gets the friendly NO-TOOLS persona). `caller` is the
    # normalized From, used to key cross-call memory. We FAIL CLOSED: only an explicit "true"
    # authorizes; anything else (missing, malformed) → no tools. (/twiml always sends "true"
    # for outbound, so the morning call is unaffected.)
    try:
        cp = start["start"].get("customParameters", {}) or {}
    except (AttributeError, TypeError):
        cp = {}
    authorized_raw = cp.get("authorized") or websocket.query_params.get("authorized") or ""
    authorized = str(authorized_raw).lower() == "true"
    caller = cp.get("caller") or websocket.query_params.get("caller") or ""

    # OUTBOUND uses the pre-generated motivational quote (with a fresh fallback if it was
    # somehow lost). INBOUND has no quote — the assistant persona never delivers one.
    if mode == "outbound":
        quote = (QUOTES.pop(call_sid, (None, 0.0))[0]) or generate_quote()
    else:
        QUOTES.pop(call_sid, None)  # discard the unused empty placeholder
        quote = ""
    greeting = INBOUND_GREETING if mode == "inbound" else GREETING
    logger.info(
        f"/ws connected stream_sid={stream_sid} call_sid={call_sid} "
        f"mode={mode} authorized={authorized}"
    )

    serializer = TwilioFrameSerializer(
        stream_sid=stream_sid,
        call_sid=call_sid,
        account_sid=os.environ["TWILIO_ACCOUNT_SID"],
        auth_token=os.environ["TWILIO_AUTH_TOKEN"],  # lets Pipecat hang the call up at the end
    )
    transport = FastAPIWebsocketTransport(
        websocket=websocket,
        params=FastAPIWebsocketParams(
            audio_in_enabled=True,
            audio_out_enabled=True,
            audio_in_sample_rate=SR,
            audio_out_sample_rate=SR,
            add_wav_header=False,
            vad_analyzer=SileroVADAnalyzer(params=VADParams(stop_secs=VAD_STOP_SECS)),
            serializer=serializer,
        ),
    )

    stt = DeepgramSTTService(api_key=os.environ["DEEPGRAM_API_KEY"], sample_rate=SR)
    llm = OpenAILLMService(
        api_key=os.environ["NVIDIA_API_KEY"],
        base_url="https://integrate.api.nvidia.com/v1",
        model=LLM_MODEL,
        params=OpenAILLMService.InputParams(
            # P2-3: match the validated m0_local_bot.py (0.2) — the scripted morning flow
            # (react -> exact quote -> short chat -> goodbye) wants focus, not creativity.
            # Lower temp also reinforces the "don't re-greet" instruction (see dry-run).
            temperature=0.2,
            top_p=0.95,
            max_tokens=4096,
            extra={"extra_body": {"chat_template_kwargs": {"enable_thinking": False}}},
        ),
    )
    # Cap the free endpoint's tail latency (documented to stall 20-30s); SDK retries fresh.
    llm._client = llm._client.with_options(timeout=8.0, max_retries=2)
    tts = CartesiaTTSService(
        api_key=os.environ["CARTESIA_API_KEY"],
        voice_id=CARTESIA_VOICE_ID,
        sample_rate=SR,
        settings=CartesiaTTSService.Settings(
            generation_config=GenerationConfig(speed=CARTESIA_SPEED)
        ),
    )

    # --- Build the system prompt + tool surface based on AUTHORIZATION --------------------
    # AUTHORIZED: full registry tools + GLM factory + cross-call memory. The persona is the
    # morning-quote flow (outbound) or the assistant flow (inbound), both with the general
    # MAC CONTROL + protocols block. We also splice in any remembered facts about this caller.
    # UNAUTHORIZED: NO tools, the friendly no-tools persona, NO factory/registry/broker/memory.
    broker = None
    recorder = None
    task_holder = _TaskHolder()
    if authorized:
        # Cross-call memory: recall() returns "" for an unknown/first-time caller (so the prompt
        # is unchanged), or a compact "Things to remember about this caller:" block we append.
        base_prompt = inbound_system_prompt() if mode == "inbound" else system_prompt(quote)
        mem = ""
        try:
            mem = agent_memory.recall(caller)
        except Exception:  # noqa: BLE001 — memory must never break the call
            mem = ""
        prompt = (base_prompt + "\n\n" + mem) if mem else base_prompt

        # Per-call confirmation broker (CONFIRM gating) + recorder (capture this call for the
        # memory store; finalized in the `finally` below).
        broker = mac_tools.ConfirmationBroker()
        recorder = agent_memory.CallRecorder(call_sid, direction=mode, caller=caller)
        tools = authorized_tools_schema()  # full registry + confirm/cancel/request_new_tool
    else:
        # No-tools persona for a caller we don't recognize. NOT_GIVEN (not None) is how pipecat's
        # universal LLMContext expresses "no tools" — passing None raises TypeError. (CONTRACT
        # said tools=None; the live API requires NOT_GIVEN, same intent: nothing is offered.)
        prompt = no_tools_system_prompt()
        tools = NOT_GIVEN

    # BUG 1 fix: seed the context so the model's history shows it has ALREADY greeted.
    # The exact GREETING goes in as the assistant's first turn — paired with the system
    # prompt's "never greet again", the model has nothing to restart and just continues.
    #
    # TOOL-NARRATION fix: for an AUTHORIZED call we ALSO seed one proper tool-call exchange
    # right after the greeting (see _seed_tool_example). Nemotron-nano imitates its own history;
    # without this, a single prose-narration turn poisons the call and the model stops calling
    # tools entirely. The seed makes 'call a tool' the established pattern and recovers tool use
    # even after a stray narration. Unauthorized callers have no tools, so they get no seed.
    seeded_messages: list[dict] = [
        {"role": "system", "content": prompt},
        {"role": "assistant", "content": greeting},
    ]
    if authorized:
        seeded_messages.extend(_seed_tool_example())
    context = LLMContext(messages=seeded_messages, tools=tools)

    # Voice-controlled Mac harness: for an AUTHORIZED call, wire the FULL registry + the
    # confirm/cancel/request_new_tool control tools onto the LLM. The adapters dispatch in a
    # worker thread (so blocking osascript never stalls the loop), record actions to memory,
    # and gate CONFIRM actions on the per-call broker. Skipped entirely for unauthorized calls.
    if authorized:
        build_tools_and_register(llm, context, broker, recorder, task_holder)
    # Patient endpointing: a prosody model (not a silence timer) decides when the caller is
    # done, so the agent waits him out instead of cutting him off or dead-airing.
    turn_strategies = UserTurnStrategies(
        stop=[
            # P1-3: reuse the preloaded ONNX model (no blocking reload inside the call).
            TurnAnalyzerUserTurnStopStrategy(turn_analyzer=build_turn_analyzer())
        ]
    )
    # user_params wires the strategy into the user half; the user aggregator still feeds
    # finished caller transcripts into the LLM exactly as before.
    aggregator = LLMContextAggregatorPair(
        context,
        user_params=LLMUserAggregatorParams(user_turn_strategies=turn_strategies),
    )

    # P0-3: pass the quote so the processor can (a) detect when it's been delivered and
    # (b) exclude its text from farewell matching.
    goodbye = GoodbyeProcessor(quote, mode=mode)  # watches the agent's completed turn for a farewell
    pipeline = Pipeline(
        [
            transport.input(),
            stt,
            aggregator.user(),
            llm,
            goodbye,
            tts,
            transport.output(),
            aggregator.assistant(),
        ]
    )
    task = PipelineTask(
        pipeline,
        params=PipelineParams(allow_interruptions=True, audio_in_sample_rate=SR, audio_out_sample_rate=SR),
    )
    goodbye.task = task  # hand the processor the task so it can queue EndFrame on goodbye
    # Hand the live task to the tool adapters so request_new_tool can queue its "building it
    # now" filler frame the moment it's called (the adapters closed over this holder above).
    task_holder.task = task

    # BUG 1 fix: speak the opening as a FIXED line exactly once — no LLMRunFrame, so an
    # interruption can never trigger a regeneration that loops the intro. We hold off until
    # the caller's audio stream is live (on_client_connected) so the first word isn't lost.
    @transport.event_handler("on_client_connected")
    async def _greet(_t, _c):
        await asyncio.sleep(0.3)
        # append_to_context=False: the greeting is already in the context above, so we don't
        # want the assistant aggregator to add it a SECOND time. `greeting` is the persona's
        # fixed opener (morning GREETING for outbound, INBOUND_GREETING when Ishaan dialed in).
        await task.queue_frames([TTSSpeakFrame(greeting, append_to_context=False)])

    @transport.event_handler("on_client_disconnected")
    async def _bye(_t, _c):
        await task.cancel()

    async def _max_duration_guard():
        await asyncio.sleep(MAX_CALL_SECS)
        # P1-2: avoid a double EndFrame. If the goodbye flow already armed/queued the hangup,
        # the call is already finishing — don't pile on a second EndFrame.
        if goodbye._armed or goodbye._ending:
            logger.info("max call duration reached but goodbye already ending the call — skip")
            return
        logger.info("max call duration reached — ending call")
        await task.queue_frames([EndFrame()])

    runner = PipelineRunner(handle_sigint=False)
    guard = asyncio.create_task(_max_duration_guard())
    try:
        await runner.run(task)
    finally:
        guard.cancel()
        # MEMORY: persist + summarize this call (authorized calls only — unauthorized ones have
        # no recorder). Best-effort and time-bounded: summarization runs in a worker thread so
        # it doesn't block the loop, and we cap the wait so teardown isn't delayed more than a
        # few seconds. Any failure is swallowed — memory must never break call teardown.
        if recorder is not None:
            try:
                messages = context.get_messages() or context.messages
            except Exception:  # noqa: BLE001
                messages = None
            try:
                await asyncio.wait_for(
                    asyncio.to_thread(recorder.finalize, messages), timeout=5.0
                )
            except Exception:  # noqa: BLE001 — incl. TimeoutError: don't delay teardown
                logger.warning("memory finalize skipped (error or timeout)")
        logger.info(f"/ws call {call_sid} finished")
