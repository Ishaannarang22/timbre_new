def _strip_quote(text: str, quote: str) -> str:
    """P0-3: remove the motivational quote (and its core sentence) from the agent text
    before farewell-matching, so words inside the quote can't arm the hangup. We strip the
    exact quote and also any long contiguous run of it, case-insensitively."""
    if not quote:
        return text
    out = text
    q = quote.strip()
    # Exact quote removal (most common: the model delivers it verbatim).
    idx = out.lower().find(q.lower())
    if idx != -1:
        out = out[:idx] + " " + out[idx + len(q) :]
    # Also strip the inner quoted sentence (between the first pair of double quotes in the
    # quote), since the model sometimes reflows surrounding words but keeps the core line.
    m = re.search(r'"([^"]{8,})"', q)
    if m:
        inner = m.group(1)
        i2 = out.lower().find(inner.lower())
        if i2 != -1:
            out = out[:i2] + " " + out[i2 + len(inner) :]
    return out


def detect_goodbye(text: str, quote: str) -> bool:
    """P0-3: True only for a GENUINE closing in the agent's turn, AFTER excluding the quote
    text. Pulled out as a free function so it can be unit-tested offline."""
    cleaned = _strip_quote(text, quote).strip()
    return bool(GOODBYE_RE.search(cleaned) or GOODBYE_TAIL_RE.search(cleaned))


class GoodbyeProcessor(FrameProcessor):
    """P0-3 fix: end the call shortly after the AGENT says a GENUINE goodbye — but only once
    the scripted flow has actually completed, so a stray farewell-ish phrase can't hang up
    mid-conversation.

    This processor sits AFTER the LLM in the pipeline and watches the agent's own output:

      - It accumulates the streamed assistant text (LLMTextFrame) for the current turn.
      - On LLMFullResponseEndFrame (the turn is complete) it:
          * counts the completed assistant turn,
          * marks the quote as delivered once the turn contains it,
          * and only ARMS the hangup if BOTH (a) the quote has been delivered AND
            (b) at least MIN_ASSISTANT_TURNS_BEFORE_GOODBYE completed turns have happened,
            AND the cleaned turn text (quote removed) matches a genuine closing.
      - On the next BotStoppedSpeakingFrame (the farewell has finished playing out of the
        TTS) it queues an EndFrame. The Twilio serializer's auto_hang_up then drops the real
        phone call. MAX_CALL_SECS stays as a backstop.

    We match on the *completed* turn (not mid-stream), exclude the quote text from matching,
    and wait for bot-stopped-speaking so we never cut the agent off mid-farewell.
    """

    def __init__(self, quote: str, mode: str = "outbound"):
        super().__init__()
        self.task: PipelineTask | None = None  # set after the task is created
        self._quote = quote
        # mode == "inbound" means Ishaan dialed us (assistant persona, NO motivational
        # quote). The outbound gate requires the quote to have been delivered before a
        # farewell may hang up; inbound has no quote, so we drop that requirement and only
        # require a genuine goodbye after at least one real exchange.
        self._mode = mode
        self._buffer = ""          # assistant text for the in-flight turn
        self._turns = 0            # completed GENERATED assistant turns so far
        self._quote_delivered = False
        self._armed = False        # farewell detected, waiting for speech to finish
        self._ending = False       # EndFrame already queued (one-shot)

    def _quote_in(self, text: str) -> bool:
        """Has the quote (or its core sentence) appeared in this turn?"""
        if not self._quote:
            return False
        # _strip_quote shortens the text iff it found the quote/inner sentence.
        return len(_strip_quote(text, self._quote)) < len(text)

    async def process_frame(self, frame: Frame, direction: FrameDirection):
        await super().process_frame(frame, direction)

        if isinstance(frame, LLMTextFrame):
            self._buffer += frame.text
        elif isinstance(frame, LLMFullResponseEndFrame):
            text = self._buffer.strip()
            self._buffer = ""
            if text:
                self._turns += 1
                if not self._quote_delivered and self._quote_in(text):
                    self._quote_delivered = True
                    logger.info("quote delivered -> goodbye gating now active")
                if self._mode == "inbound":
                    # No quote in the assistant flow when Ishaan calls in: a genuine
                    # goodbye after at least one real generated exchange may hang up. We
                    # still require >=1 completed turn so a stray farewell-ish phrase in
                    # the model's very first reply can't drop the call instantly.
                    gate_ok = self._turns >= 1
                else:
                    gate_ok = (
                        self._quote_delivered
                        and self._turns >= MIN_ASSISTANT_TURNS_BEFORE_GOODBYE
                    )
                if not self._armed and gate_ok and detect_goodbye(text, self._quote):
                    logger.info(
                        f"genuine goodbye detected (turn {self._turns}, quote delivered) "
                        f"-> will hang up: {text!r}"
                    )
                    self._armed = True
                elif not self._armed and detect_goodbye(text, self._quote):
                    logger.info(
                        f"farewell-ish phrase ignored (gate not met: "
                        f"quote_delivered={self._quote_delivered} turns={self._turns}): {text!r}"
                    )
        elif (
            isinstance(frame, BotStoppedSpeakingFrame)
            and self._armed
            and not self._ending
            and self.task is not None
        ):
            self._ending = True
            logger.info("farewell finished speaking -> queueing EndFrame (auto hang up)")
            await self.task.queue_frame(EndFrame())

        await self.push_frame(frame, direction)


