# --- CALLER AUTHORIZATION (owner-only Mac control) ----------------------------
# Tools are OFFERED to the model ONLY for an AUTHORIZED call (CONTRACT.md "Caller
# authorization"): the 7 AM OUTBOUND call (we dialed the owner) is always authorized; an
# INBOUND call is authorized IFF the caller's number is on the allowlist. An unauthorized
# caller gets a friendly NO-TOOLS persona — it can chat but cannot control this Mac.
# AUTHORIZED_CALLERS is a comma-separated E.164 list; it DEFAULTS to TARGET_PHONE_NUMBER so
# the owner's own number works out of the box. Caller ID can be spoofed, so this is
# access-control (not crypto) — the per-call ws token still blocks non-Twilio scanners.
def _normalize_e164(num: str | None) -> str:
    """Normalize a phone number to DIGITS ONLY for allowlist comparison — drop the '+', spaces,
    and punctuation so '+1 (415) 555-0100', '+14155550100', and '14155550100' all compare equal.
    Digits-only is deliberately forgiving: an owner-allowlist match must not fail just because
    Twilio sends '+E.164' while AUTHORIZED_CALLERS/TARGET_PHONE_NUMBER was written without the '+'
    (or with spacing). Used both for the allowlist check and as the cross-call memory key."""
    if not num:
        return ""
    return re.sub(r"\D", "", str(num))


def _authorized_callers() -> set[str]:
    """The normalized allowlist of numbers permitted to control the Mac on an INBOUND call.
    Read fresh each call so a .env change doesn't require a restart. Defaults to
    TARGET_PHONE_NUMBER when AUTHORIZED_CALLERS is unset."""
    raw = os.getenv("AUTHORIZED_CALLERS") or os.getenv("TARGET_PHONE_NUMBER") or ""
    return {_normalize_e164(p) for p in raw.split(",") if p.strip()}


def is_authorized(direction: str, from_number: str | None) -> bool:
    """True if this call may use tools. OUTBOUND (anything not 'inbound') is always authorized —
    we placed the call to the owner. INBOUND is authorized only if From is on the allowlist."""
    if str(direction).lower() != "inbound":
        return True
    return _normalize_e164(from_number) in _authorized_callers()

